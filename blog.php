      <I><p>Disability affects more than one billion people worldwide, according to the United Nations. Disability inclusion means understanding the relationship between the way people function and how they participate in society and making sure everybody has the same opportunities to participate in every aspect of life to the best of their abilities and desires.
</p><p>
In alignment with this concept of inclusion, we at Bhumi decided to celebrate the festival of lights at Vidyasagar (Kotturpuram) with our friends in wheelchairs. It was made clear that it’s going to be an inclusive celebration with music, games, fun activities, & an informative session on supporting differently-abled members. We started with a prayer song sung by one of the volunteers, followed by a speech from Ms. Ummul Kher (advocate) who requested the audience for their support and encouragement towards the differently-abled members. 
</p><P>
The fun began with a relay game where teams were formed with equal participation from Bhumi volunteers and Vidyasagar members. This was enjoyed by everyone with lots of hoots & cheering and some yummy refreshments afterward. The evening continues with songs and dances performed by our volunteers and relished by the Vidyasagar members. </p>
