</P><p>
The most recent session was not only an opportunity for children to showcase their creativity, but also tap into their fine and gross locomotor skills for higher-order skills such as sequencing, hypothesis testing, analysis, and evaluation. It also built confidence & developed negotiation and communication skills in children (verbal and non-verbal). The materials used in the activity enabled children to realise how waste can aid in experiential learning – be it cereal boxes, egg boxes, bottle tops, foil trays, product packaging, or small pipes, fabric remnants, buttons, etc.</p></i>
