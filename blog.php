      <i><p>Lakshya programme helps to enrich the lives of underprivileged children (mentees) through a caring relationship with a personal mentor who supports, encourages, and builds their self-confidence. It also enables children from classes 8 and above to complete the cycle of growth by guiding them to focus on a career of their choice or need. 
